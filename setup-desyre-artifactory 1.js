// Improved environment variable loader with better error handling and variable substitution
function loadEnvLocal(envPath = '.env.local') {
  try {
    if (!fs.existsSync(envPath)) {
      console.warn(`Environment file ${envPath} not found`)
      return
    }

    const envContent = fs.readFileSync(envPath, 'utf8')
    let loadedCount = 0
    const envVars = {}

    // First pass: load all variables
    envContent.split('\n').forEach((line, index) => {
      const trimmedLine = line.trim()
      if (!trimmedLine || trimmedLine.startsWith('#')) return

      const match = trimmedLine.match(/^\s*([\w.-]+)\s*=\s*(.*)\s*$/)
      if (match) {
        const key = match[1]
        let value = match[2].replace(/(^['"]|['"]$)/g, '')
        envVars[key] = value
      } else if (trimmedLine) {
        console.warn(`Invalid line ${index + 1} in ${envPath}: ${line}`)
      }
    })

    // Second pass: resolve variable substitutions and set environment variables
    Object.entries(envVars).forEach(([key, value]) => {
      // Handle variable substitution like ${VAR_NAME}
      let resolvedValue = value
      const variablePattern = /\$\{([^}]+)\}/g
      let match

      while ((match = variablePattern.exec(value)) !== null) {
        const varName = match[1]
        const varValue = process.env[varName] || envVars[varName] || ''
        resolvedValue = resolvedValue.replace(match[0], varValue)
      }

      if (!process.env[key]) {
        process.env[key] = resolvedValue
        loadedCount++
        console.log(`Loaded ${key}=${resolvedValue}`)
      }
    })

    console.log(`Loaded ${loadedCount} environment variables from ${envPath}`)
  } catch (error) {
    console.error(`Error loading ${envPath}:`, error.message)
  }
}

// Validate URL format
function isValidUrl(string) {
  try {
    new URL(string)
    return true
  } catch {
    return false
  }
}

