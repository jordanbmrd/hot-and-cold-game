// Create .npmrc with proper error handling
function createNpmrcConfig() {
  const registryUrl = `https://${ARTIFACTORY_DOMAIN}/artifactory/api/npm/${DESYRE_PROJECT_ID}/`
  const npmrcPath = '.npmrc'

  try {
    // Backup existing .npmrc if it exists
    if (fs.existsSync(npmrcPath)) {
      const backupPath = `${npmrcPath}.backup.${Date.now()}`
      fs.copyFileSync(npmrcPath, backupPath)
      console.log(`Backed up existing .npmrc to ${backupPath}`)
    }

    const npmrcContent =
      [
        `registry=${registryUrl}`,
        `http-proxy=${HTTPS_PROXY}`,
        `https-proxy=${HTTPS_PROXY}`,
        `# Generated by setup-desyre-artifactory.js on ${new Date().toISOString()}`
      ].join('\n') + '\n'

    fs.writeFileSync(npmrcPath, npmrcContent)
    console.log(`Created ${npmrcPath} with registry: ${registryUrl}`)
  } catch (error) {
    console.error(`Error creating ${npmrcPath}:`, error.message)
    process.exit(1)
  }
}

// Fetch authentication with better error handling and retry logic
async function fetchAuthWithRetry(maxRetries = 3) {
  const authUrl = `https://${ARTIFACTORY_USERNAME}:${ARTIFACTORY_TOKEN}@${ARTIFACTORY_DOMAIN}/artifactory/api/npm/auth`

  for (let attempt = 1; attempt <= maxRetries; attempt++) {
    try {
      console.log(
        `Fetching authentication data (attempt ${attempt}/${maxRetries})...`
      )

      const authData = execFileSync(
        'curl',
        [
          '--proxy',
           HTTPS_PROXY,
          '--silent',
          '--show-error',
          '--fail', // Fail on HTTP error codes
          '--max-time',
          '30', // 30 second timeout
          authUrl
        ],
        {
          encoding: 'utf8',
          timeout: 35000 // 35 second timeout for the process
        }
      )

      if (!authData.trim()) {
        throw new Error('Empty authentication response')
      }

      fs.appendFileSync('.npmrc', authData)
      console.log('Successfully added authentication to .npmrc')
      return
    } catch (error) {
      console.warn(`Attempt ${attempt} failed:`, error.message)

      if (attempt === maxRetries) {
        console.error('Failed to fetch authentication after all retries')
        console.error('Please check your credentials and network connection')
        process.exit(1)
      }

      // Wait before retry (exponential backoff)
      const delay = Math.pow(2, attempt) * 1000
      console.log(`Waiting ${delay}ms before retry...`)
      await new Promise(resolve => setTimeout(resolve, delay))
    }
  }
}

// Main execution with better structure
async function main() {
  try {
    console.log('Setting up Artifactory configuration...')

    createNpmrcConfig()
    await fetchAuthWithRetry()

    console.log('Running npm config fix...')
    execSync('npm config fix', { stdio: 'inherit' })

    console.log('✅ Artifactory setup completed successfully!')
  } catch (error) {
    console.error('❌ Setup failed:', error.message)
    process.exit(1)
  }
}

// Run the script
main()
